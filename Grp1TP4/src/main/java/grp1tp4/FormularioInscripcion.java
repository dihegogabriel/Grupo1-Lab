    private void armarCabecera(){ 
    
        modelo.addColumn("Fecha");
        modelo.addColumn("Materia");
        modelo.addColumn("Alumno");
        jtInscripciones.setModel(modelo);       
    }

    private void cargarDatos(Inscripcion inscripcion){ 
                
        modelo.addRow(new Object[]{inscripcion.getFecha(), inscripcion.getMateria(),inscripcion.getAlumno()});
    }
    
    private void cargarCombo(){ 
                
        for (Alumno alumno : listaAlumnos) {
            jcAlumno.addItem(alumno);;           
        }
        for (Materia materia : listaMaterias) {
            jcMateria.addItem(materia);;           
        }
        
    }

