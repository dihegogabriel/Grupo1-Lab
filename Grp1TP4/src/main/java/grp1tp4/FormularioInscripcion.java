    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed

        if(jcMateria.getSelectedObjects().equals(null)||jcAlumno.getSelectedObjects().equals(null)){
            JOptionPane.showMessageDialog(this,"Seleccione el alumno y la materia para poder guardar");
            return;
        }

        Alumno aluSelec = (Alumno) jcAlumno.getSelectedItem();
        Materia matSelec = (Materia) jcMateria.getSelectedItem();
        int aluN= aluSelec.getLegajo();
        int matN= matSelec.getIdMateria();
        Inscripcion incN = new Inscripcion(LocalDate.now(), matN, aluN);

        for (Inscripcion inscripcion : listaInscripciones) {
            if(inscripcion.getAlumno().equalsIgnoreCase(incN.getAlumno()) && inscripcion.getMateria().equals(incN.getMateria())){
                JOptionPane.showMessageDialog(this,"Esta inscripciòn ya ha sido realizada anteriormente");
                return;
            }
        }

        listaInscripciones.add(incN);
        cargarDatos(incN);

        // TODO add your handling code here:

    }//GEN-LAST:event_jbInscribirActionPerformed

